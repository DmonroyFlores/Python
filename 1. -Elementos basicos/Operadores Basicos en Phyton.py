#Aritmeticos: +, -, *, /, **, %, //
a = 3 + 10  #suma
b = 56 - 28 #resta
c = 2 * 5   #multiplicacion 
d = 2**3    #exponencial
e = c / d   #division con decimal
f = c % d   #modulo
g = c//d    #division entera
print(a)
print(b)
print(c)
print(d)
print(e)
print(f)
print(g)


#Logicos: and, or, not
a = 3
a > 2 and a < 0 #and (¿se cumple a y b?)
a > 2 or a < 0  #or (¿se cumple a o b?)
not a > 2       #not (no al valor)


#Comparativos: ==, !=, >, <, >=, <=
b = 4
print (a == b)  #igual que
print (a != b)  #distinto que
print (a > b)   #mayor que
print (a < b)   #menor que
print (a >= b)  #mayor o igual que 
print (a <= b)  #menor o igual que 


